class typePromotion {
    public static void main(String[] args) {
        // int / int = int 
        // int / float = float
        // int * float = float
        // short * short = int
        // short * int = int
        // short * long = long
        // byte * byte = int
        System.out.println(5.2/3);
        byte b=120; 
        byte c=120;
        System.out.println(b*c); //14400
    }
}